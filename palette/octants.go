// blockPixels — октант-эквиваленты из Block Elements / Legacy Computing:
// popcount в модели 2×4. Вместе с octantPixels задаёт всё семейство глифов,
// которые раскладываются в одну страницу по плотности.
var blockPixels = map[rune]int{
	'▘': 2, '▝': 2, '▖': 2, '▗': 2,
	'▀': 4, '▄': 4, '▌': 4, '▐': 4, '▚': 4, '▞': 4,
	'▙': 6, '▛': 6, '▜': 6, '▟': 6,
	'█': 8, '▂': 2, '▆': 6, '🮂': 2, '🮅': 6,
}

// pixelCount возвращает плотность глифа семейства 2×4 и true, если он к нему принадлежит.
func pixelCount(g rune) (int, bool) {
	if g >= 0x1CD00 && g <= 0x1CDE5 {
		return int(octantPixels[g-0x1CD00]), true
	}
	n, ok := blockPixels[g]
	return n, ok
}
